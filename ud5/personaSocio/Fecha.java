package cd2023.ud5.personaSocio;

public class Fecha {
    private int dia;
    private int mes;
    private int ano;

    //GETTERS & SETTERS
    public int getDia() {
        return dia;
    }
    public void setDia(int dia) {
        this.dia = dia;
    }
    public int getMes() {
        return mes;
    }
    public void setMes(int mes) {
        this.mes = mes;
    }
    public int getAno() {
        return ano;
    }
    public void setAno(int ano) {
        this.ano = ano;
    }
}
